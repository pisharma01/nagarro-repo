CREATE TABLE USER_DTLS (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USERNAME NVARCHAR2(128) NOT NULL,
    FIRSTNAME NVARCHAR2(128) NOT NULL,
    LASTNAME NVARCHAR2(128)  NULL,
    USERTYPE NVARCHAR2(50) NOT NULL,
    EMAIL NVARCHAR2(128)  NULL,
    ADDRESS NVARCHAR2(300)  NULL,
    PHONE NVARCHAR2(128)  NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CREATED_BY NVARCHAR2(128)  NULL,
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY NVARCHAR2(128)  NULL,
    PRIMARY KEY(ID)
);

ALTER TABLE USER_DTLS
