CREATE TABLE PRODUCT (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    PRODUCT_CODE NVARCHAR2(128) NOT NULL,
    PRODUCT_NAME NVARCHAR2(128) NOT NULL,
    PRODUCT_TYPE NVARCHAR2(128)  NULL,
    UNITS_AVAILABLE NVARCHAR2(50) NOT NULL,
    DESCRIPTION NVARCHAR2(128)  NULL,
    PRICE NUMBER(10,2)  NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CREATED_BY NVARCHAR2(128)  NULL,
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY NVARCHAR2(128)  NULL,
    PRIMARY KEY(ID)
);